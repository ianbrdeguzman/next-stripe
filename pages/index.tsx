import Head from 'next/head';
import Link from 'next/link';
import { useCart } from '../hooks/useCart';
import getAllProducts from '../lib/getAllProducts';

export interface ProductData {
  id: string;
  name: string;
  description: string;
  price: number;
}

export default function Home({ products }: { products: ProductData[] }) {
  const { cart, addItemToCart } = useCart();
  console.log(cart);
  return (
    <div>
      <Head>
        <title>Next | Stripe</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        {products.map((p) => (
          <article
            key={p.id}
            className="my-2 border p-4 rounded max-w-xs cursor-pointer"
          >
            <Link href={`/products/${p.id}`}>
              <a>
                <h2 className="text-2xl">{p.name}</h2>
                <p>{p.description}</p>
                <p>${p.price / 100}</p>
              </a>
            </Link>
            <button
              type="button"
              className="border p-1 rounded mt-4"
              onClick={() => addItemToCart({ id: p.id, qty: 1 })}
            >
              Add to Cart
            </button>
          </article>
        ))}
      </main>
    </div>
  );
}

export async function getStaticProps() {
  const products = await getAllProducts();

  return {
    props: {
      products
    }
  };
}
